# Declaring numbers and strings to some variables
types_of_people = 10
x = f"There are {types_of_people} types on people."
binary = "binary"
do_not = "don't"
y = f"Those who know {binary} and those who {do_not}."

# Printing the variables with some formatting
print(x)
print(y)
print(f"I said: {x}")
print(f"I also said: {y}")

# Declaring some more variables
hilarious = False
joke_evaluation = "Isn't this joke so funny!? {}"

# Some more printing with a more interesting format
print(joke_evaluation.format(hilarious))

# Declaring variables yet again
w = "This is the left side of..."
